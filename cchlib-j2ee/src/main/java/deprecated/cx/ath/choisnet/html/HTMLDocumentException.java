package deprecated.cx.ath.choisnet.html;


public class HTMLDocumentException extends Exception
{

    private static final long serialVersionUID = 1L;

    public HTMLDocumentException(String message)
    {
        super(message);
    }

    public HTMLDocumentException(Throwable cause)
    {
        super(cause);
    }

    public HTMLDocumentException(String message, Throwable cause)
    {
        super(message, cause);
    }
}
